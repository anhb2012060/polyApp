    private void initTable(){
        tableModel = new DefaultTableModel();
        String[] row = new String[]{"Mã sinh viên", "Họ tên","Email","Số điện thoại","Giới tính","Địa chỉ"};
        tableModel.setColumnIdentifiers(row);
        jTable_QLSV.setModel(tableModel);
    }
    private void loadDataToTable(){
        try {
            SinhVienDao dao = new SinhVienDao();
            List<SinhVien> list = dao.findAll();
            tableModel.setRowCount(0);
            for(SinhVien sv : list){
                tableModel.addRow(new Object[]{
                    sv.getMaSinhVien(),sv.getHoTen(),sv.getEmail(),sv.getSoDienThoai(),
                    sv.getGioiTinh()==1 ?"Nam":"Nữ",sv.getDiaChi()
                });
            }
            tableModel.fireTableDataChanged();
        } catch (Exception e) {
        }
    }
    
    public int findRowIndexById(String maSinhVien){
        for(int row = 0; row < jTable_QLSV.getRowCount(); row++){
            String maSinhVienInTable = (String) jTable_QLSV.getValueAt(row, 0);
            if(maSinhVien.equals(maSinhVienInTable)){
                return row;
            }
        }
        return -1;
    }
