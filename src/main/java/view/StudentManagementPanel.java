    private void jButton_LuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LuuActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validatorEmpty(jTextField_MaSinhVien, sb, "Mã sinh viên không được trống");
        DataValidator.validatorEmpty(jTextField_HoTen, sb, "Tên sinh viên không được để trống");
        
        if(sb.length() > 0){
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return ;
        }
        try {
            SinhVien sv = new SinhVien();
            sv.setMaSinhVien(jTextField_MaSinhVien.getText());
            sv.setHoTen(jTextField_HoTen.getText());
            sv.setEmail(jTextField_Email.getText());
            sv.setSoDienThoai(jTextField_SoDienThoai.getText());
            sv.setGioiTinh(jRadioButton_Nam.isSelected() ? 1 : 0);
            sv.setDiaChi(jTextArea_DiaChi.getText());
            sv.setHinh(personalImage);
            if (SinhVienDao.insertStudent(sv)) {
                MessageDialogHelper.showMessageDialog(this, "Thêm mới sinh viên thành công", "Thông báo");
                loadDataToTable();
            } else {
                MessageDialogHelper.showErrorDialog(this, "Sinh viên không được lưu do có lỗi", "Cảnh báo");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
        
    }//GEN-LAST:event_jButton_LuuActionPerformed

    private void jButton_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_TimKiemActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validatorEmpty(jTextField_MaSinhVien, sb, "Mã sinh viên không được trống");
        
        if(sb.length() > 0){
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return ;
        }
        try {
            SinhVien sv = SinhVienDao.findByIdStudent(jTextField_MaSinhVien.getText());
            if(sv == null){
                MessageDialogHelper.showMessageDialog(this, "Không tìm thấy sinh viên", "Thông báo");
            } else {
                jTextField_MaSinhVien.setText(sv.getMaSinhVien());
                jTextField_HoTen.setText(sv.getHoTen());
                jTextField_Email.setText(sv.getEmail());
                jTextField_SoDienThoai.setText(sv.getSoDienThoai());
                jRadioButton_Nam.setSelected(sv.getGioiTinh()==1);
                jRadioButton_Nu.setSelected(sv.getGioiTinh()==0);
                jTextArea_DiaChi.setText(sv.getDiaChi());
    //            ------------------Hình--------------------
    
    //Chọn hàng trong bảng: 
                int rowIndex = findRowIndexById(jTextField_MaSinhVien.getText());
                if(rowIndex>=0){
                    jTable_QLSV.setRowSelectionInterval(rowIndex, rowIndex);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jButton_TimKiemActionPerformed

