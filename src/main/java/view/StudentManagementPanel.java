    private void jButton_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_XoaActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validatorEmpty(jTextField_MaSinhVien, sb, "Mã sinh viên không được trống");
        
        if(sb.length() > 0){
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return ;
        }
        if(MessageDialogHelper.showConfirmDialog(this, "Bạn có muốn xóa sinh viên này ?", "Thông báo") == JOptionPane.NO_OPTION){
            return ;
        }
        try {
            if(SinhVienDao.deleteStudent(jTextField_MaSinhVien.getText())){
                MessageDialogHelper.showMessageDialog(this, "Xóa sinh viên thành công", "Thông báo");
                jButton_TaoMoiActionPerformed(evt);
                loadDataToTable();
            } else {
                MessageDialogHelper.showErrorDialog(this, "Sinh viên không xóa được do có lỗi", "Thông báo");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jButton_XoaActionPerformed

    private void jTable_QLSVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_QLSVMouseClicked
        try {
            int row = jTable_QLSV.getSelectedRow();
            if(row>=0){
                String maSinhVien = (String) jTable_QLSV.getValueAt(row, 0);
                SinhVien sv = SinhVienDao.findByIdStudent(maSinhVien);
                if(sv!=null){
                    jTextField_MaSinhVien.setText(sv.getMaSinhVien());
                    jTextField_HoTen.setText(sv.getHoTen());
                    jTextField_Email.setText(sv.getEmail());
                    jTextField_SoDienThoai.setText(sv.getSoDienThoai());
                    jRadioButton_Nam.setSelected(sv.getGioiTinh()==1);
                    jRadioButton_Nu.setSelected(sv.getGioiTinh()==0);
                    jTextArea_DiaChi.setText(sv.getDiaChi());
                    if(sv.getHinh()!=null){
                        Image img = ImageHelper.createImageFromByArray(sv.getHinh(), "png");
                        jLabel_Hinh.setIcon(new ImageIcon(img));
                        personalImage = sv.getHinh();
                    } else {
                        personalImage = sv.getHinh();
                        ImageIcon icon = new ImageIcon(getClass().getResource("/com/mycompany/icon/student.png"));
                        jLabel_Hinh.setIcon(icon);
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jTable_QLSVMouseClicked

    private void jButton_MoHinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_MoHinhActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(new FileFilter() {
            @Override
            public boolean accept(File f) {
                if(f.isDirectory()){
                    return true;
                } else {
                    return f.getName().toLowerCase().endsWith(".png");
                }
            }

            @Override
            public String getDescription() {
                return "Image file (*.png)";
            }
        });
        if(chooser.showOpenDialog(parentForm) == JFileChooser.CANCEL_OPTION){
            return;
        }
        File file = chooser.getSelectedFile();
        try {
            ImageIcon icon = new ImageIcon(file.getPath());
            Image img = ImageHelper.resize(icon.getImage(), 140, 140);
            ImageIcon resizedIcon = new ImageIcon(img);
            jLabel_Hinh.setIcon(icon);
            personalImage = ImageHelper.toByArray(img, "png");
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showMessageDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jButton_MoHinhActionPerformed
    
