        if(sb.length() > 0){
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return ;
        }
        try {
            BangDiem bangDiem = new BangDiem();
            bangDiem.setMaSinhVien(jTextField_MaSinhVien.getText());
            bangDiem.setTiengAnh(Float.parseFloat(jTextField_TiengAnh.getText()));
            bangDiem.setTinHoc(Float.parseFloat(jTextField_TinHoc.getText()));
            bangDiem.setGDTC(Float.parseFloat(jTextField_GDTC.getText()));
            
            if(BangDiemDao.updateBangDiem(bangDiem)){
                MessageDialogHelper.showMessageDialog(this, "Cập nhật thông tin điểm thành công", "Thông báo");
                loadDataToTable();
            } else {
                MessageDialogHelper.showErrorDialog(this, "Cập nhật thông điểm không thành công", "Lỗi");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
