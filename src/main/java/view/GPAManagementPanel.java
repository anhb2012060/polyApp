    private void jButton_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_TimKiemActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validatorEmpty(jTextField_MaSinhVien_Search, sb, "Mã sinh viên không được trống");
        if(sb.length() > 0){
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return ;
        }
        
        try {
            SinhVien sv = SinhVienDao.findByIdStudent(jTextField_MaSinhVien_Search.getText());
            BangDiem bangDiem = BangDiemDao.findById(jTextField_MaSinhVien_Search.getText());
            jTextField_MaSinhVien.setText(bangDiem.getMaSinhVien());
            jTextField_HoTen.setText(sv.getHoTen());
            jTextField_TiengAnh.setText(bangDiem.getTiengAnh()+"");
            jTextField_TinHoc.setText(bangDiem.getTinHoc()+"");
            jTextField_GDTC.setText(bangDiem.getGDTC()+"");
            jTextField_MaSinhVien.setEditable(false);
        } catch (Exception e) {
            MessageDialogHelper.showErrorDialog(getParent(), "Thông tìm thấy thông tin điểm sinh viên", "Thông báo");
        }
    }//GEN-LAST:event_jButton_TimKiemActionPerformed

    private void jTextField_GDTCFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField_GDTCFocusLost
        if(jTextField_TiengAnh.getText().equals("") || jTextField_TinHoc.getText().equals("") || jTextField_GDTC.getText().equals("")){
            return ;
        }
        float ta = Float.parseFloat(jTextField_TiengAnh.getText());
        float th = Float.parseFloat(jTextField_TinHoc.getText());
        float tc = Float.parseFloat(jTextField_GDTC.getText());
        
        float tbinh = (ta+th+tc)/3;
        
        String st = String.format("%.2f",tbinh);
        jLabel_TrungBinh.setText(st);
    }//GEN-LAST:event_jTextField_GDTCFocusLost

    private void jButton_LuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LuuActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validatorEmpty(jTextField_MaSinhVien, sb, "Mã sinh viên không được trống");
        if(sb.length() > 0){
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return ;
        }
        try {
            BangDiem bangDiem = new BangDiem();
            bangDiem.setMaSinhVien(jTextField_MaSinhVien.getText());
            bangDiem.setTiengAnh(Float.parseFloat(jTextField_TiengAnh.getText()));
            bangDiem.setTinHoc(Float.parseFloat(jTextField_TinHoc.getText()));
            bangDiem.setGDTC(Float.parseFloat(jTextField_GDTC.getText()));
            
            if(BangDiemDao.saveBangDiem(bangDiem)){
                MessageDialogHelper.showMessageDialog(this, "Thêm thông tin điểm thành công", "Thông báo");
                loadDataToTable();
            } else {
                MessageDialogHelper.showErrorDialog(this, "Thông điểm không thêm thành công", "Lỗi");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton_LuuActionPerformed

