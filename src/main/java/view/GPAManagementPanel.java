    private void jButton_LocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LocActionPerformed
        try {
            if(jTextField_SoLuong.getText().equals("")){
                loadDataToTable();
            }
            int soLuong = Integer.parseInt(jTextField_SoLuong.getText());
            List<BangDiem> list = BangDiemDao.findTopBangDiem(soLuong);
            if(soLuong>list.size()){
                MessageDialogHelper.showMessageDialog(this, "Số lượng cần lọc lớn hơn số lượng sinh viên", "Thông báo");
                return;
            }
            tblModel.setRowCount(0);
            for(BangDiem bangDiem : list){
                float tb = (bangDiem.getTiengAnh()+bangDiem.getTinHoc()+bangDiem.getGDTC())/3;
                String st = String.format("%.2f", tb);
                SinhVien sv = SinhVienDao.findByIdStudent(bangDiem.getMaSinhVien());
                tblModel.addRow(new Object[]{
                   bangDiem.getMaSinhVien(),sv.getHoTen(),bangDiem.getTiengAnh(),bangDiem.getTinHoc(),bangDiem.getGDTC(),st
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_jButton_LocActionPerformed

